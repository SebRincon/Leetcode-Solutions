func productExceptSelf(nums []int) []int {
    res := make ([]int, len(nums))

    for i := range res {
        res[i] = 1 
    }


    prefix := 1
    for idx, num := range(nums){
        res[idx] = prefix 
        prefix *= num
    }

    

    postfix := 1
    for i := len(nums)-1; i>=0; i-- {
        res[i] *= postfix
        postfix *= nums[i]
    }

    return res
    }

